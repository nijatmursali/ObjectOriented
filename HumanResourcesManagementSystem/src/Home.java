    public void CurrentDate() {
        Thread clock = new Thread() {
            public void run() {
                for(;;) {
                    Calendar calend = new GregorianCalendar();
                    int month = calend.get(Calendar.MONTH);
                    int year = calend.get(Calendar.YEAR);
                    int day = calend.get(Calendar.DAY_OF_MONTH);
                    
                    jMenu4.setText("Date "+day+"-"+(month+1)+"-"+year);
                    
                    int hour = calend.get(Calendar.HOUR);
                    int minutes= calend.get(Calendar.MINUTE);
                    int seconds = calend.get(Calendar.SECOND);
                    
                    jMenu5.setText("Time "+hour + ":"+(minutes) +":" +seconds);
                    try {
                        sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Home.class.getName()).log(Level.SEVERE,null, ex);
                    }
                }
            }
        };
        clock.start();
    }

